CREATE VIEW [dbo].[v_00_id_approve_cancel_before]
AS
SELECT     approve.ID AS 'CA APPROVE'
FROM         (SELECT     ID, CIF, Value_Customer
                       FROM          dbo.v_Credit_Account_Process_Rank
                       WHERE      (SUBJECT_DETAIL_ID IN ('101', '102', '103')) AND (PROCESS_GROUP IN ('C')) AND (PROCESS_ID IN ('C020', 'C050')) AND (RANK = 1) AND 
                                              (Value_Customer > 0)) AS cancel INNER JOIN
                          (SELECT     ID, CIF, Value_Customer
                            FROM          dbo.v_Credit_Account_Process_Rank AS v_Credit_Account_Process_Rank_1
                            WHERE      (SUBJECT_DETAIL_ID IN ('101', '102', '103')) AND (PROCESS_GROUP IN ('D')) AND (RANK = 1) AND (Value_Customer > 0)) AS approve ON 
                      cancel.CIF = approve.CIF AND cancel.Value_Customer = approve.Value_Customer
GROUP BY cancel.CIF, cancel.ID, approve.ID

GO
