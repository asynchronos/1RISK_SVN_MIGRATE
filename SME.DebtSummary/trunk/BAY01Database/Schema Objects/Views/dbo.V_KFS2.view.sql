CREATE VIEW dbo.V_KFS2
AS
SELECT     TOP (100) PERCENT A.GROUP_ID, A.ID, A.ModelName, A.CIF, A.CIF0, CASE WHEN CHARINDEX(',', A.CIF, 1) 
                      = 0 THEN ModelName WHEN LEN(RTRIM(C.CFIRST_E)) > 0 THEN dbo.GetNameEng(C.CTITLE_E, C.CFIRST_E, C.CLAST_E) 
                      ELSE dbo.GetName(C.CUS_TITLE, C.CUS_FIRST, C.CUS_LAST) END AS NAME_E, A.DomainName, A.CM_Name, A.stmtdate, A.revenue, A.NetIncome, 
                      A.DE, A.Unit, ISNULL
                          ((SELECT     SUM(DISTINCT CONVERT(money, revenue)) AS SUM_RE
                              FROM         dbo.V_KFS AS T2
                              WHERE     (ID IN
                                                        (SELECT     ID
                                                          FROM          dbo.V_KFS AS T
                                                          WHERE      (GROUP_ID = A.GROUP_ID)))), A.revenue) AS SUM_RE
FROM         dbo.V_KFS AS A LEFT OUTER JOIN
                      dbo.CUSTOMER AS C ON A.CIF0 = C.CIF

GO
