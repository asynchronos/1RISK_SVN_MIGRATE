CREATE VIEW CA_REPORT.V_009_SUM_PREVIOUS_LIMIT_FROM_CLS
AS
SELECT MONTH_KEY, LEADER1_CA_OLD_ID, CIF, PRODUCT_NAME, COUNT(PRODUCT_NAME) AS COUNT_PRODUCT, 
               SUM(LIMIT_AMOUNT_PRODUCT_THB) AS SUM_LIMIT_AMOUNT_PRODUCT_THB
FROM  CA_REPORT.PREVIOUS_1_MONTH_LIMIT_OF_FINISHED_CA AS L
WHERE EXISTS
                   (SELECT DISTINCT OLD_ID, CIF
                    FROM   CA_REPORT.V_007_ACCOUNT_CA_BOARD_FINISHED AS V007
                    WHERE (OLD_ID = L.LEADER1_CA_OLD_ID) AND (CIF = L.CIF))
GROUP BY MONTH_KEY, CIF, LEADER1_CA_OLD_ID, PRODUCT_NAME

