CREATE VIEW CUSTOMER.V_002_GROUP_SIZE_DETAIL
AS
SELECT A.GROUP_ID, A.GROUP_SIZE_ID, C.DESCRIPTION AS GROUP_SIZE_DESC, A.UPDATE_DATE, A.UPDATE_BY, A.GROUP_EXPOSURE, A.NEXT_REVIEW_DATE, 
               CIF AS MAIN_CIF
FROM  [dbo].[GROUP_SIZE] C INNER JOIN
               dbo.CUSTOMER_SME_GROUP A ON C.GROUP_SIZE_ID = A.GROUP_SIZE_ID INNER JOIN
                   (SELECT GROUP_ID, CIF
                    FROM   (SELECT G.[GROUP_ID], S.CIF, ROW_NUMBER() OVER (PARTITION BY G.GROUP_ID
                                    ORDER BY CIF) AS SEQ
                    FROM   [dbo].[CUSTOMER_SME_GROUP] G INNER JOIN
                                   [dbo].[CUSTOMER_SME] S ON G.GROUP_ID = S.GROUP_ID) AS DISPLAY_CIF
WHERE SEQ = 1) AS B ON A.GROUP_ID = B.GROUP_ID

GO
